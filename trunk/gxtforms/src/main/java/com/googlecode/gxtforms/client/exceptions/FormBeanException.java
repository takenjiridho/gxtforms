package com.googlecode.gxtforms.client.exceptions;

@SuppressWarnings("serial")
public class FormBeanException extends RuntimeException {

    public FormBeanException() {
        super();
    }

    public FormBeanException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public FormBeanException(String arg0) {
        super(arg0);
    }

    public FormBeanException(Throwable arg0) {
        super(arg0);
    }

}
