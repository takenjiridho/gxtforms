package com.googlecode.gxtforms.client.exceptions;



@SuppressWarnings("serial")
public class FieldConfigurationException extends FormConfigurationException {

    public FieldConfigurationException() {
        super();

    }

    public FieldConfigurationException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

    public FieldConfigurationException(String arg0) {
        super(arg0);

    }

    public FieldConfigurationException(Throwable arg0) {
        super(arg0);

    }

}
