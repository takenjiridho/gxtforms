package com.googlecode.gxtforms.client.exceptions;


@SuppressWarnings("serial")
public class FormConfigurationException extends RuntimeException {

    public FormConfigurationException() {
        super();

    }

    public FormConfigurationException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

    public FormConfigurationException(String arg0) {
        super(arg0);

    }

    public FormConfigurationException(Throwable arg0) {
        super(arg0);

    }

}
